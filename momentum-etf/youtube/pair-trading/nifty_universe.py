# Mapping for easy access
NIFTY_UNIVERSES = {"nifty50": NIFTY_50, "nifty100": NIFTY_100, "nifty200": NIFTY_200}


def get_nifty_universe(universe_name):
    """
    Get stocks for a specific Nifty universe

    Args:
        universe_name (str): One of 'nifty50', 'nifty100', 'nifty200'

    Returns:
        list: List of stock symbols with .NS suffix

    Raises:
        ValueError: If universe_name is not recognized
    """
    universe_name = universe_name.lower()
    if universe_name not in NIFTY_UNIVERSES:
        raise ValueError(
            f"Unknown universe: {universe_name}. Available: {list(NIFTY_UNIVERSES.keys())}"
        )

    return NIFTY_UNIVERSES[universe_name].copy()


def get_available_universes():
    """Get list of available universe names"""
    return list(NIFTY_UNIVERSES.keys())


def get_universe_info():
    """Get information about all universes"""
    return {universe: len(stocks) for universe, stocks in NIFTY_UNIVERSES.items()}


